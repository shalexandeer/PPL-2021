def banyakBro(n1,n2):
    print('Nilai A adalah', n1)
    print('Nilai B adalah', n2)
    print('A pangkat B adalah', n1**n2)
    print('A modulus B adalah', n1%n2);
    print('A sama dengan B adalah', n1 == n2)
    print('A Lebih Besar dari B adalah', n1 >= n2)
    print('A kurang dari B adalah', n1 <= n2)
    print('A Tidak Sama dengan B adalah', n1 != n2)

banyakBro(9,2)